<h1>About GetShop eCommerce</h1>
The GetShop eCommerce wordpress plugin is developed and maintained by the GetShop team.<br>
<br>
The plugin is storing all its content in the GetShop cloud so if your istallation get lost you can simply reinstall and it will all be there. <br>
The GetShop cloud also handles email messaging, store transactions, payment gateways and everything else that is a pain to handle manually. This makes it super simple to set up and start selling your products.<br>
<br>
Any question or feedback for the plugin can be forwarded to <a href='mailto:post@getshop.com'>post@getshop.com</a>.<br>
<br>
<h1>Using the plugin</h1>
1. Configure the settings needed for your store under the settings tab.<br>
2. Add your products under the products tab.<br>
3. Add the shortcodes where you would like to use it <br>
